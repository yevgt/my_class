class Movie:
    def __init__(self, title, release_year, category, length, rental_rate, rating, rating_description, description):
        self.title = title
        self.release_year = release_year
        self.category = category
        self.length = length
        self.rental_rate = rental_rate
        self.rating = rating
        self.rating_description = rating_description
        self.description = description